'''
int -> inteiro ->  numero sem casas decimais
float -> ponto flutuante -> possui casas decimais
aqui segue o mesmo principio matemático, se um numero não apresenta sinal, ele é
positivo.

caso deseje verificar o dado, pode apelar para a classe type() veja os exemplos
'''
# int e positivo
print(11)

# int e negativo
print(-11)

# float e positivo
print(15.6)

# float e negativo
print(-15.6)

# checando type
print(type(15))

# checando type
print(type(15.5))